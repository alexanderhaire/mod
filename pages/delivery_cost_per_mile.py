            # SOP30300 = Sales History Line (has item, qty, uom, extended cost)
            # RM00102 = Customer Address Master (has actual address for PRSTADCD / ADRSCODE)
            # We join to get the ship-to address details per invoice
            query = """
            SELECT
                h.SOPNUMBE,
                h.DOCDATE,
                h.CUSTNMBR,
                h.CUSTNAME,
                h.PRSTADCD,
                l.ITEMNMBR,
                l.ITEMDESC,
                l.QUANTITY,
                l.UOFM,
                l.XTNDPRCE,
                l.EXTDCOST,
                COALESCE(NULLIF(LTRIM(RTRIM(addr.CITY)),''),     LTRIM(RTRIM(h.CITY)))     AS ShipCity,
                COALESCE(NULLIF(LTRIM(RTRIM(addr.STATE)),''),    LTRIM(RTRIM(h.STATE)))    AS ShipState,
                COALESCE(NULLIF(LTRIM(RTRIM(addr.ZIP)),''),      LTRIM(RTRIM(h.ZIPCODE)))  AS ShipZip,
                COALESCE(NULLIF(LTRIM(RTRIM(addr.ADDRESS1)),''), LTRIM(RTRIM(h.ADDRESS1))) AS ShipAddress1,
                h.SHIPMTHD       AS ShipMethod
            FROM SOP30200 h
            JOIN SOP30300 l
                ON h.SOPNUMBE = l.SOPNUMBE AND h.SOPTYPE = l.SOPTYPE
            LEFT JOIN RM00102 addr
                ON h.CUSTNMBR = addr.CUSTNMBR AND h.PRSTADCD = addr.ADRSCODE
            WHERE h.DOCDATE BETWEEN ? AND ?
              AND h.SOPTYPE = 3
              AND h.VOIDSTTS = 0
              AND l.QUANTITY > 0
              AND LTRIM(RTRIM(h.SHIPMTHD)) = 'CDI'
            ORDER BY h.DOCDATE DESC
            """
            cursor = conn.cursor()
            cursor.execute(query, [start, end])
            rows = cursor.fetchall()
            cols = [d[0] for d in cursor.description]
            df = pd.DataFrame.from_records(rows, columns=cols)
            conn.close()

            if not df.empty:
                df["DOCDATE"] = pd.to_datetime(df["DOCDATE"])
                # Convert numeric columns (Decimal from SQL Server -> float)
