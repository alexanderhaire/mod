def get_gas_price_for_date(gas_df, target_date):
    """Find the closest gas price for a given delivery date."""
    if gas_df.empty:
        return None
    target = pd.to_datetime(target_date)
    idx = (gas_df["period"] - target).abs().idxmin()
    return gas_df.loc[idx, "gas_price"]


