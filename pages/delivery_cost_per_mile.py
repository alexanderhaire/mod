    """Geocode a street address to (lat, lon) via Nominatim (OpenStreetMap)."""
    try:
        url = "https://nominatim.openstreetmap.org/search"
        r = requests.get(url, params={"q": address, "format": "json", "limit": 1},
                         headers={"User-Agent": "CDI-DeliveryCostApp/1.0"}, timeout=5)
        data = r.json()
        if data:
            return (float(data[0]["lat"]), float(data[0]["lon"]))
    except Exception:
        pass
    return None


