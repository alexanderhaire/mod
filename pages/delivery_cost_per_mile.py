@st.cache_data(ttl=86400, show_spinner=False)
def get_driving_miles_by_address(destination_address):
    """Get real driving distance via Google Distance Matrix API using street addresses."""
    api_key = load_project_secrets().get("google", {}).get("maps_api_key", "")
    if api_key and destination_address:
        try:
            url = "https://maps.googleapis.com/maps/api/distancematrix/json"
            params = {
                "origins": PLANT_ADDRESS,
                "destinations": destination_address,
                "key": api_key,
            }
            r = requests.get(url, params=params, timeout=10)
            data = r.json()
            if data.get("status") == "OK":
                element = data["rows"][0]["elements"][0]
                if element.get("status") == "OK":
                    meters = element["distance"]["value"]
                    return round(meters / 1609.344, 1)
        except Exception:
            pass
    return None


def haversine_miles(lat1, lon1, lat2, lon2):
    """Calculate distance in miles using the haversine formula."""
    R = 3958.8  # Earth radius in miles
    lat1, lon1, lat2, lon2 = map(math.radians, [lat1, lon1, lat2, lon2])
    dlat = lat2 - lat1
    dlon = lon2 - lon1
    a = math.sin(dlat / 2) ** 2 + math.cos(lat1) * math.cos(lat2) * math.sin(dlon / 2) ** 2
    return round(R * 2 * math.asin(math.sqrt(a)), 1)


def calc_distance_miles(coords1, coords2):
    """Calculate distance in miles between two (lat, lon) tuples."""
    if not coords1 or not coords2:
        return None
    return haversine_miles(coords1[0], coords1[1], coords2[0], coords2[1])


